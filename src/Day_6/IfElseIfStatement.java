package Day_6;

public class IfElseIfStatement {
    public static void main(String[] args) {
        int x = 9;
        if(x > 0){
            System.out.println("It is greater than 0");
        }
        else if(x < 0){
            System.out.println("It is less than 0");
        }
        else{
            System.out.println("It is equal to 0");
        }
    }
}
